// Create two arrays of your choice.
// Use the concat method to combine the two arrays into a new array.
// Print both the original arrays and the combined array using console.log().

// Original arrays
const bangladeshiFruits = ['Mango', 'Banana', 'Jackfruit'];
const importedFruits = ['Apple', 'Orange', 'Kiwi'];

// Combine the arrays
const allFruits = bangladeshiFruits.concat(importedFruits);

// Output
console.log("Bangladeshi Fruits:", bangladeshiFruits);
console.log("Imported Fruits:", importedFruits);
console.log("All Fruits (Combined):", allFruits);
