// 3. Checking Array Membership with ‘includes’
// Instructions:

// Create an array of books containing different book.

// Use the includes method to check if the array contains a javascript book.

// Print a message to the console indicating whether the element is present in the array or not.

// Step 1: Create an array of books
let books = ['Python Basics', 'HTML & CSS', 'JavaScript', 'React Guide', 'Node.js'];

// Step 2: Check if the array includes 'JavaScript'
if (books.includes('JavaScript')) {
  console.log('✅ The book "JavaScript" is present in the array.');
} else {
  console.log('❌ The book "JavaScript" is NOT present in the array.');
}
